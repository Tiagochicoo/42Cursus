#include "libft.h"

void	*memset(void *s, int c, size_t n)
{
	unsigned char	*p;

	*p = s;
	while (n > 0)
	{
		*p = c;
		p++;
		n--;
		if (*p == 0)
			break ;
	}
	return (s);
}
